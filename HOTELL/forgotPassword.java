    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int check=0;
        String securityQuestion=jTextField2.getText();
        String answer=jTextField3.getText();
        String newPassword=jPasswordField1.getText();
        if(answer.equals("") || newPassword.equals(""))
        {
            check=1;
            JOptionPane.showMessageDialog(null, "All Fields Is Required");
        }
        else
        {
            ResultSet rs=Select.getData("select * from users where email ='"+email+"' and securityQuestion='"+securityQuestion+"' and  answer='"+answer+"'");
            try
            {
               if(rs.next()) 
               {
                 check=1;  
                 InsertUpdateDelete.setData("update users set Password='"+newPassword+"' where email='"+email+"'", "Password has been changed successfully.");
                 setVisible(false);
                 new forgotPassword().setVisible(true);
               }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        if(check==0)
            JOptionPane.showMessageDialog(null, "The answer to the security question is wrong.");
      
    }//GEN-LAST:event_jButton3ActionPerformed

